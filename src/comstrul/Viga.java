package comstrul;

public class Viga {
	private double alturaViga ;
	private double larguraViga;
    private double comprimentoViga ;
	public double getAlturaViga() {
		return alturaViga;
	}
	public void setAlturaViga(double alturaViga) {
		this.alturaViga = alturaViga;
	}
	public double getLarguraViga() {
		return larguraViga;
	}
	public void setLarguraViga(double larguraViga) {
		this.larguraViga = larguraViga;
	}
	public double getComprimentoViga() {
		return comprimentoViga;
	}
	public void setComprimentoViga(double comprimentoViga) {
		this.comprimentoViga = comprimentoViga;
	}
} 
